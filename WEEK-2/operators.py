""" - Operators
    - Assignment Operators
    - Arithmetic Operators
    - Comparison Operators
    - Logical Operators """

# Arithmetic Operators
print(1 + 2)
print(2 - 1)
print(2 * 3)
print(3 / 2)
print(3 % 2)
print(7 // 4)
print(2 ** 3)
print(2 ** 5)

# Assignment Operators
print(10)
a = 100
print(a)
a += 10 # -=, *=, /=
print(a)

# Comparison Operators

print(3 > 2)
print(2 < 3)
print(2 <= 3)
print(3 >= 2)
print(3 < 2)
print(3 <= 2)
print(3 == 3)
print(3 == '3')
print(3 == int('3'))
print(str(3) == '3')

# Logical Operators (and , or not)
print(3 > 2 and 2 > 1)
print(3 > 2 and 2 < 1)
print(3 > 2 or 1 < 0)
print(3 < 2 or 1 < 0)
print(not not False)
print( not 4 > 5)
print(not(3 < 2 or 1 < 0))
print(1 is 1)
print(1 is not 2)
print('land' in 'Finland')
print('Eth' in 'Ethiopia')